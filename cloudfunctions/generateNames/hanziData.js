/**
 * 获取字的五行属性
 * @param {string} char - 汉字
 * @returns {string} 五行属性（金木水火土）
 */
function getCharWuxing(char) {
  // 优先查找数据库
  if (WUXING_DATA[char]) {
    return WUXING_DATA[char]
  }

  // 如果数据库中没有，基于康熙字典规则计算
  // 这里使用简化规则，实际应该查询完整的康熙字典
  console.warn(`字"${char}"五行数据缺失，使用默认值`)
  const defaultWuxing = ['金', '木', '水', '火', '土']
  const hash = char.split('').reduce((acc, c) => acc + c.charCodeAt(0), 0)
  return defaultWuxing[hash % 5]
}

